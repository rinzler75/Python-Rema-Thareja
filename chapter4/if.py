char=input("Press any key: ")
if(char.isalpha()): #to check if the character is alphabet or not
	print("The user has entered a character")
if(char.isdigit()): #to check if the character is digit or not
	print("The user has entered a digit")
if(char.isspace()): #if a space is entered
	print("The user entered a white space character")

